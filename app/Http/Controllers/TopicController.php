    {
         $t=new topic();
        $t->topicname=$request->topicname;
        if(!$t->save())
        {
            $err=$t->getErrors();
            return redirect()->action('TopicController@create')->with('errors', $err)->withInput();
        }
        return redirect()->action('TopicController@create')->with('message', 'New topic'.$t->country.' has been added with id='.$t->id.'!')->withInput();
